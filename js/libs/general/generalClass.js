
function showNotivicationBox(text) {
        document.getElementById("notificationbox").style.display = "block";
        document.getElementById("notificationbox").innerHTML = text;

}

function showNotivicationBoxRed(text) {
        document.getElementById("notificationbox").style.display = "block";
        document.getElementById("notificationbox").style.backgroundColor = "#CC0000";
        document.getElementById("notificationbox").innerHTML = text;

}
