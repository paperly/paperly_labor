            <p>Die Allianz Arena baut mit Zusammenarbeit der Telekom die Mobilfunkversorgung im Stadion&nbsp;drastisch aus. <br><br>Während einem Fußballspiel der Bayern Profis sollen problemlos über 50.000 Zuschauer auf ein lokales WLAN-Netzwerk zugreifen können, was weltweit einzigartig wäre in einem Fußballstadion.&nbsp;<br><br>Zudem sollen&nbsp;die Bayern-Fans bei den Heimspielen ihres Klubs über die neue Mobilfunk-Technologie LTE mit Höchstgeschwindigkeit im Netz surfen. Der Münchner Fußball-Tempel ist das erste Stadion in Deutschland, dass die Telekom mit dem neuen Highspeed-Standard versorgt. Die Mobilfunkversorgung mit den Standards GSM und UMTS wird zudem ebenfalls&nbsp;aufgestockt.<br><br>Damit ist es nun kein Problem mehr&nbsp;während des Fußballspiels Videos zu streamen, Bilder zu teilen und Spielstände in anderen Stadien nachsehen zu können.&nbsp;<br><br><span>Nebenbei wollen der FC Bayern München und die Telekom mit "StadiumVision" die Münchner Arena zu einem multimedialen Erlebnisraum für alle Fußball-Fans machen. Die Kommunikation soll ab Mitte 2014&nbsp;die Arena schrittweise für eine neuartige Form der Kommunikation mit den Fans vor, während und nach den Spielen sorgen. <br><br>Zusätzliche Informationen zum Spiel, Live-Streams, Video- Wiedergaben oder Szenen aus früheren Partien sowie Unterhaltungsangebote werden auf zusätzlichen Bildschirmen überall im Stadion und auf dem eigenen Smartphone zu empfangen sein.</span><br><br><br></p>
